public class LaboratorioX {

    
    public void ejercicioX_cuadrado(int n){
    
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                System.out.print("* ");
            }//endFor
            System.out.println("");
        }//endFor
    
    }//endMethod
    
}//endClass
